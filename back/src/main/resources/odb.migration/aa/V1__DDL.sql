CREATE TABLE CLASSROOM
(
    ID             BIGINT  NOT NULL,
    ACTIVE         BOOLEAN NOT NULL,
    BUILDING       SMALLINT,
    CLASSROOM_NAME VARCHAR(200),
    CREATED_DATE   TIMESTAMP,
    DESCRIPTION    VARCHAR(2000),
    MODIFIED_DATE  TIMESTAMP,
    CONSTRAINT CONSTRAINT_A PRIMARY KEY (ID)
);

CREATE TABLE CONTACT
(
